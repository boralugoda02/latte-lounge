/* =========================================================
   LATTE LOUNGE by 7's — content.js
   Fetches dynamic JSON files generated by the Decap CMS 
   under /assets/data/ and renders them into the pages.
   ========================================================= */

document.addEventListener('DOMContentLoaded', () => {
  renderOffers();
  renderMenu();
  renderSettings();
});

async function loadJSON(path) {
  const response = await fetch(path, { cache: 'no-store' });
  if (!response.ok) throw new Error(`Failed to load ${path}`);
  return response.json();
}

/** Renders dynamic CMS Offers & Announcements on the homepage. */
async function renderOffers() {
  const grid = document.getElementById('offersGrid');
  if (!grid) return;

  try {
    const data = await loadJSON('assets/data/offers.json');
    const headingEl = document.getElementById('offers-heading');
    if (headingEl && data.title) {
      headingEl.textContent = data.title;
    }

    const offers = data.cards || [];
    if (offers.length === 0) {
      grid.innerHTML = '<p class="lead">No active offers at the moment. Check back soon!</p>';
      return;
    }

    grid.innerHTML = offers.map((offer) => {
      const tagClass = `tag-${(offer.tag || 'coffee').toLowerCase()}`;
      return `
        <article class="offer-card">
          <span class="tag-chip ${tagClass}">${escapeHtml(offer.tag)}</span>
          <h3>${escapeHtml(offer.heading)}</h3>
          <p>${escapeHtml(offer.description)}</p>
        </article>
      `;
    }).join('');
  } catch (error) {
    console.error('Error rendering offers:', error);
    grid.innerHTML = '<p class="lead">Check back soon for our latest happenings.</p>';
  }
}

/** Fetches menu items from assets/data_menu/menu.json */
async function fetchMenuItems() {
  try {
    const data = await loadJSON('assets/data_menu/menu.json');
    return data.items || [];
  } catch (err) {
    console.warn('Failed to load menu from assets/data_menu/menu.json, trying fallbacks:', err);
  }

  // Fallback 1: assets/data/menu.json
  try {
    const data = await loadJSON('assets/data/menu.json');
    if (Array.isArray(data)) return data;
    return data.items || [];
  } catch (err) {
    console.warn('Fallback static menu.json fetch failed, trying root data/items.json:', err);
  }

  // Fallback 2: data/items.json
  try {
    const data = await loadJSON('data/items.json');
    return (data.items || []).map(item => ({
      title: item.name,
      price: parseInt(String(item.price).replace(/[^0-9]/g, ''), 10) || 750,
      description: item.description,
      category: String(item.tag).toUpperCase(),
      featured: item.featured ?? true
    }));
  } catch (err) {
    console.error('All menu loading fallback operations failed:', err);
    return [];
  }
}

function getCategoryImage(category) {
  const cat = String(category || '').toUpperCase();
  if (cat === 'COFFEE') return 'assets/images/item-latte.jpg';
  if (cat === 'CAKE') return 'assets/images/item-lovecake.jpg';
  if (cat === 'SNACK') return 'assets/images/item-cutlets.jpg';
  return 'assets/images/item-latte.jpg';
}

/** Renders the menu items with a dynamic filter interface */
async function renderMenu() {
  const grid = document.getElementById('itemsGrid');
  if (!grid) return;

  try {
    const items = await fetchMenuItems();
    if (items.length === 0) {
      grid.innerHTML = '<p class="lead">Our signature items are temporarily offline. Visit us to see today\'s fresh batch.</p>';
      return;
    }

    // Insert category filter tabs dynamically above the items grid
    const filterContainer = document.createElement('div');
    filterContainer.className = 'menu-tabs';
    filterContainer.setAttribute('role', 'tablist');
    filterContainer.setAttribute('aria-label', 'Filter cafe menu items');
    filterContainer.innerHTML = `
      <button class="menu-tab is-active" role="tab" aria-selected="true" data-category="ALL">All Items</button>
      <button class="menu-tab" role="tab" aria-selected="false" data-category="COFFEE">Coffee</button>
      <button class="menu-tab" role="tab" aria-selected="false" data-category="CAKE">Cakes</button>
      <button class="menu-tab" role="tab" aria-selected="false" data-category="SNACK">Snacks</button>
    `;
    grid.parentNode.insertBefore(filterContainer, grid);

    // Render items
    renderMenuItemsGrid(items);

    // Add filter behavior
    const tabs = filterContainer.querySelectorAll('.menu-tab');
    tabs.forEach(tab => {
      tab.addEventListener('click', () => {
        tabs.forEach(t => {
          t.classList.remove('is-active');
          t.setAttribute('aria-selected', 'false');
        });
        tab.classList.add('is-active');
        tab.setAttribute('aria-selected', 'true');

        const category = tab.getAttribute('data-category');
        const cards = grid.querySelectorAll('.item-card');
        cards.forEach(card => {
          const cardCat = card.getAttribute('data-category');
          if (category === 'ALL' || cardCat === category) {
            card.style.display = 'flex';
          } else {
            card.style.display = 'none';
          }
        });
      });
    });

  } catch (error) {
    console.error('Error rendering menu items:', error);
    grid.innerHTML = '<p class="lead">Could not load menu items. Please try refreshing.</p>';
  }
}

function renderMenuItemsGrid(items) {
  const grid = document.getElementById('itemsGrid');
  if (!grid) return;

  grid.innerHTML = items.map((item, idx) => {
    const catClass = `tag-${(item.category || 'coffee').toLowerCase()}`;
    const imgUrl = getCategoryImage(item.category);
    const displayPrice = `LKR ${item.price.toLocaleString()}`;
    
    return `
      <article class="item-card" data-category="${escapeAttr(item.category)}" data-index="${idx}">
        <div class="item-media">
          <img src="${imgUrl}" alt="${escapeAttr(item.title)}" loading="lazy">
        </div>
        <div class="item-body">
          <div class="item-top">
            <h3>${escapeHtml(item.title)}</h3>
            <span class="item-price">${escapeHtml(displayPrice)}</span>
          </div>
          <span class="tag-chip ${catClass}" style="align-self:flex-start;">${escapeHtml(item.category)}</span>
          <p class="item-desc">${escapeHtml(item.description)}</p>
          <div class="item-action" style="margin-top: auto; padding-top: 1rem;">
            <button class="btn btn-primary add-to-cart-btn" 
                    data-title="${escapeAttr(item.title)}" 
                    data-price="${item.price}" 
                    data-category="${escapeAttr(item.category)}"
                    data-image="${imgUrl}"
                    style="width: 100%; justify-content: center; font-size: 0.85rem; padding: 0.65rem 1rem;">
              <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" style="width: 16px; height: 16px; margin-right: 4px;" aria-hidden="true">
                <circle cx="9" cy="21" r="1"/><circle cx="20" cy="21" r="1"/>
                <path d="M1 1h4l2.68 13.39a2 2 0 0 0 2 1.61h9.72a2 2 0 0 0 2-1.61L23 6H6"/>
              </svg>
              Add to Order
            </button>
          </div>
        </div>
      </article>
    `;
  }).join('');
}

/** Fills in hours, phone, address, map, and social links everywhere they appear using assets/data/content.json */
async function renderSettings() {
  const targets = document.querySelectorAll('[data-field]');
  if (!targets.length) return;

  try {
    const settings = await loadJSON('assets/data/content.json');

    targets.forEach((el) => {
      const field = el.getAttribute('data-field');
      const value = field.split('.').reduce((obj, key) => (obj ? obj[key] : undefined), settings);
      if (value === undefined) return;

      if (el.tagName === 'A' && el.hasAttribute('href')) {
        el.setAttribute('href', field === 'phoneHref' ? `tel:${value}` : value);
        return;
      }
      if (el.tagName === 'IFRAME') {
        el.setAttribute('src', value);
        return;
      }
      el.textContent = value;
    });
  } catch (error) {
    console.error('Error rendering settings:', error);
  }
}

function escapeHtml(str) {
  return String(str ?? '').replace(/[&<>"']/g, (ch) => ({
    '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;',
  }[ch]));
}

function escapeAttr(str) {
  return escapeHtml(str);
}
